text = "Ella sabe Python"
# print(text[1000]) string index out of range
print(text[-1])

# Slicing
print(text[0:5])
print(text[10:16])
print(text[:10]) # Se puede obviar el inicio y quitar el parametro de inicio ejem: 0:10 => :10
print(text[5:])
print(text[:])
print(text[10:16:1])
print(text[10:16:2])
print(text[::2])