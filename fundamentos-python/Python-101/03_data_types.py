# String
my_name = "Guillermo"
my_name = "Gabriela"
print("my_name => ", my_name)
print(type(my_name))

# int
my_age = 30
print("my_age =>", my_age)
print(type(my_age))

# boolean
is_single = True
is_single = False
print("is_single =>",is_single)
print(type(is_single))

